package JavaSessions;

public class StringConcatenation {

	public static void main(String[] args) {

		String s = "Hello world";
		String s1 = "100";
		String f = "12.44";
		int a = 100;
		int b = 200;
		String x = "Hello";
		String y = "Selenium";
		System.out.println(a + b);
		System.out.println(x + y);
		System.out.println(x + a);
		System.out.println(a + b + x + y);
		System.out.println(x + y + a + b);
		System.out.println(x + y + (a + b));
		System.out.println("The value of a is:" + a);
		System.out.println("The value of b is:" + b);
		System.out.println("The value of a and b is:" + a + b);
		System.out.println("The value of a and b is:" + (a + b));
		double d1 = 12.22;
		double d2 = 22.44;
		System.out.println(a + b + x + y + d1 + d2);
		System.out.println(x + y + a + b + d1 + d2);
		System.out.println(x + d1 + d2 + y + a + b);
		System.out.println(x + (d1 + d2) + y + (a + b));
		System.out.println(d1 + d2);
		char c1 = 'a';
		char c2 = 'b';
		System.out.println(x + c1);
		System.out.println(c1 + c2);
		char c3 = '4';
		char c4 = '5';
		System.out.println(c3 + c4);
		char space = ' ';
		System.out.println(1000);
		char gender = 'f';
		System.out.println("Gender is : " + gender);
		System.out.println(c1 + "" + c2);
		System.out.println("hi this is \"sai\" here");
		System.out.println("hi this is \'sai\' here");
		int m = 9;
		int n = 3;
		System.out.println(m / n);
		System.out.println(9 / 2);
		System.out.println(9 / 2.0);
		System.out.println(9.0 / 2);
		System.out.println(9.0 / 2.0);
		System.out.println(8 % 2);
		// System.out.println(9 / 0);
		System.out.println("Bye");
		System.out.println(0 / 9);
		System.out.println(9 % 2);

	}

}
